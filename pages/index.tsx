import type { NextPage } from "next";
import Head from "next/head";
import Script from "next/script";
import React from "react";
import styles from "../styles/Home.module.css";

import type { createFFmpeg, fetchFile, FFmpeg } from "@ffmpeg/ffmpeg";

declare global {
  interface Window {
    FFmpeg: {
      createFFmpeg: typeof createFFmpeg;
      fetchFile: typeof fetchFile;
    };
  }
}

const Home: NextPage = () => {
  const [ffmpeg, setFFmpeg] = React.useState<FFmpeg>(null!);

  const loadFFmpeg = async () => {
    const { createFFmpeg } = window.FFmpeg;
    const ffmpeg = createFFmpeg({
      log: true,
      corePath: "/ffmpeg/ffmpeg-core.js",
    });
    setFFmpeg(ffmpeg);
    await ffmpeg.load();
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Script
        src="/ffmpeg/ffmpeg.min.js"
        onLoad={() => {
          loadFFmpeg();
        }}
      />

      <main className={styles.main}>Test</main>
    </div>
  );
};

export default Home;
